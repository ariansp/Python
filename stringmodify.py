#Uppercase
a = "Hello, World!"
print(a.upper())

#Lowercase
a = "Hello, World!"
print(a.lower())

#Remove whitespace from begin or end
a = "   Hello, World! "
print(a.strip())

#Replace string and change to another string
a = "Hello, World!"
print(a.replace("W", "Sw"))

#Split string with the word inside the separator
a = "Hello, World!, Welcome!"
print(a.split(","))